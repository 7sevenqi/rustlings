// 添加生命周期参数 'a，表明返回的引用与输入的两个引用中生命周期较短的那个绑定
fn longest<'a>(x: &'a str, y: &'a str) -> &'a str {
    if x.len() > y.len() {
        x
    } else {
        y
    }
}

